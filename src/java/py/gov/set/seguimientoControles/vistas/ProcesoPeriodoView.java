public class ProcesoPeriodoView {
    
    private int codObligacion;
    private String nombreObligacion;
    private String periodo;

    public int getCodObligacion() {
        return codObligacion;
    }

    public void setCodObligacion(int codObligacion) {
        this.codObligacion = codObligacion;
    }

    public String getNombreObligacion() {
        return nombreObligacion;
    }

    public void setNombreObligacion(String nombreObligacion) {
        this.nombreObligacion = nombreObligacion;
    }

    public String getPeriodo() {
        return periodo;
    }

    public void setPeriodo(String periodo) {
        this.periodo = periodo;
    }
    
    
    
}
